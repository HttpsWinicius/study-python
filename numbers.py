'''
Python possui 2 tipos de numeros: Inteiros e Pontos flutuantes
'''

print(f'Isso eh considerado um numero inteiro em python: {type(1)}')

print(f'Isso eh considerado um ponto flutuante em python: {type(1.0)}')

print(f'Isso eh considerado um numero complexo em python: {type(1 + 1j)}')

## Operacoes com numeros
print(f'Adicao: {1 + 1}')
print(f'Subtracao: {1 - 1}')
print(f'Multiplicacao: {2 * 2}')
print(f'Divisao: {10 / 5}, resultado da divisao eh sempre um ponto flutuante')
print(f'Potenciacao: {3 ** 2}')
print(f'Modulo: {10 % 3}')
print(f'Divisao inteira: {10 // 3}, resultado da divisao com // eh sempre um inteiro')
print(f'Ordem de precedencia: {2 + 10 * 10 + 3}')